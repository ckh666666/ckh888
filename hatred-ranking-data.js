// 获取仇恨值排行榜数据
window.getHatredRanking = function() {
    // 按同意票率从高到低排序（仇恨值越高排名越靠前）
    return window.hatredRankingData.legislators.sort((a, b) => b.agree_rate - a.agree_rate);
};

// 根据立委姓名查找仇恨值排名
window.getLegislatorHatredRank = function(legislatorName) {
    const ranking = window.getHatredRanking();
    const legislator = ranking.find(item => item.name === legislatorName);
    return legislator ? ranking.indexOf(legislator) + 1 : null;
};

// 获取立委的仇恨值数据
window.getLegislatorHatredData = function(legislatorName) {
    return window.hatredRankingData.legislators.find(item => item.name === legislatorName);
};

